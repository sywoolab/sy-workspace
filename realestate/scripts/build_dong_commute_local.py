    # 서울 지역 역만 필터 (서울 바운더리 대략: 위도 37.42~37.70, 경도 126.76~127.18)
    seoul_stations = {
        k: v for k, v in station_coords.items()
        if 37.42 <= v[0] <= 37.72 and 126.76 <= v[1] <= 127.22
    }
    print(f"[INFO] 서울 범위 역: {len(seoul_stations)}개")

    # 목적지 설정
    # 1) 여의도역 (5호선, 9호선)
    yeouido_starts = []
    for node in ["여의도_5호선", "여의도_9호선"]:
        if node in station_coords:
            yeouido_starts.append((node, 0))

    # 2) 청계산입구역 (신분당선)
    cheongye_starts = []
    cheongye_node = "청계산입구_신분당선"
    if cheongye_node in station_coords:
        cheongye_starts.append((cheongye_node, 0))

    # 3) 독립문역 (3호선)
    doklip_starts = []
    doklip_node = "독립문_3호선"
    if doklip_node in station_coords:
        doklip_starts.append((doklip_node, 0))

    print(f"[INFO] 목적지 노드: 여의도={len(yeouido_starts)}, 청계산입구={len(cheongye_starts)}, 독립문={len(doklip_starts)}")

    # 역방향 다익스트라 (목적지에서 출발 → 모든 역까지의 최단시간)
    dist_yeouido = dijkstra(graph, yeouido_starts)
    dist_cheongye = dijkstra(graph, cheongye_starts)
    dist_doklip = dijkstra(graph, doklip_starts)

    # 법정동 데이터 로드
    dong_coords_path = "/Users/sywoo/sy-workspace/realestate/data/dong_coords.json"
    with open(dong_coords_path, "r", encoding="utf-8") as f:
        dong_coords = json.load(f)

    print(f"[INFO] 법정동 {len(dong_coords)}개 로드")

    # 각 법정동에 대해 최근접역 + 통근시간 계산
    result = {}
    failed = []

    for dong_name, coords in dong_coords.items():
        lat, lon = coords["lat"], coords["lon"]
        nearest_node, nearest_dist_km = find_nearest_station(lat, lon, station_coords)

        if nearest_node is None:
            failed.append(dong_name)
            continue

        walk_min = walk_minutes(nearest_dist_km)
        station_name, line_name = nearest_node.rsplit("_", 1)

        # 각 목적지까지 최단시간
        t_yeouido = dist_yeouido.get(nearest_node, None)
        t_cheongye = dist_cheongye.get(nearest_node, None)
        t_doklip = dist_doklip.get(nearest_node, None)

        # None이면 해당 역에서 목적지까지 경로 없음 → 큰 값
        yeouido_total = (walk_min + t_yeouido) if t_yeouido is not None else 999
        cheongye_total = (walk_min + t_cheongye) if t_cheongye is not None else 999
        doklip_total = (walk_min + t_doklip) if t_doklip is not None else 999

        result[dong_name] = {
            "yeouido": yeouido_total,
            "cheongye": cheongye_total,
            "doklip": doklip_total,
            "nearest_station": station_name,
            "nearest_line": line_name,
            "walk_min": walk_min,
        }

    # 저장
    output_path = "/Users/sywoo/sy-workspace/realestate/data/dong_commute.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)

    print(f"\n[결과] dong_commute.json 저장 완료: {output_path}")
    print(f"  - 총 법정동: {len(dong_coords)}개")
    print(f"  - 매핑 성공: {len(result)}개")
    print(f"  - 매핑 실패: {len(failed)}개", failed if failed else "")

    # 검증 출력
    print("\n===== 검증 샘플 =====")
    samples = [
        "영등포구_여의도동",
        "영등포구_당산동",
        "서초구_서초동",
        "서초구_양재동",
        "노원구_상계동",
        "도봉구_방학동",
        "강남구_역삼동",
        "종로구_사직동",
        "마포구_합정동",
        "송파구_잠실동",
    ]

    print(f"\n{'법정동':<20} {'여의도':>8} {'청계산':>8} {'독립문':>8} {'최근접역':<15} {'호선':<10} {'도보':>4}")
    print("-" * 85)
    for s in samples:
        if s in result:
            r = result[s]
            print(f"{s:<20} {r['yeouido']:>6}분 {r['cheongye']:>6}분 {r['doklip']:>6}분 {r['nearest_station']:<15} {r['nearest_line']:<10} {r['walk_min']:>3}분")
        else:
            print(f"{s:<20} 데이터 없음")

    # 상식 검증
    print("\n===== 상식 검증 =====")
    if "영등포구_여의도동" in result:
        y = result["영등포구_여의도동"]["yeouido"]
        status = "OK" if 5 <= y <= 10 else "WARNING"
        print(f"  여의도동→여의도역: {y}분 ({status}, 기대: 5~10분)")

    for dob in ["도봉구_방학동", "도봉구_도봉동", "도봉구_쌍문동"]:
        if dob in result:
            y = result[dob]["yeouido"]
            status = "OK" if y >= 50 else "WARNING"
            print(f"  {dob}→여의도역: {y}분 ({status}, 기대: 50분+)")
            break



def dijkstra(graph, start_nodes):
    """
    여러 출발 노드에서의 최단거리를 계산한다 (역방향 다익스트라).
    start_nodes: [(node_id, initial_cost), ...]
    반환: { node_id: min_cost }
    """
    dist = {}
    heap = []
    for node, cost in start_nodes:
        if node in dist and dist[node] <= cost:
            continue
        dist[node] = cost
        heapq.heappush(heap, (cost, node))

    while heap:
        d, u = heapq.heappop(heap)
        if d > dist.get(u, float("inf")):
            continue
        for v, w in graph.get(u, []):
            nd = d + w
            if nd < dist.get(v, float("inf")):
                dist[v] = nd
                heapq.heappush(heap, (nd, v))

    return dist



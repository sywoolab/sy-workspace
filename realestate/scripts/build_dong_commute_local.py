def haversine_km(lat1, lon1, lat2, lon2):
    """두 좌표 간 거리 (km)"""
    R = 6371.0
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat / 2) ** 2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon / 2) ** 2
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))


def find_nearest_station(lat, lon, station_coords):
    """법정동 좌표에서 가장 가까운 역을 찾는다."""
    best_dist = float("inf")
    best_node = None
    for node_id, (slat, slon) in station_coords.items():
        d = haversine_km(lat, lon, slat, slon)
        if d < best_dist:
            best_dist = d
            best_node = node_id
    return best_node, best_dist


def walk_minutes(dist_km):
    """거리 기반 도보 시간 추정 (1km = 15분, 최소 5분, 최대 15분)"""
    minutes = dist_km * 15
    return max(5, min(15, round(minutes)))


